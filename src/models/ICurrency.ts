export interface ICurrency {
    /**
     * - ID of the currency
     * @example 85ad13f7-3d1b-5128-9eb2-7cd8ee0b5741
     */
    id: string,
    /**
     * - Name of the currency
     * @example Valorant Points
     */
    name: string,
    /**
     * - Amount of the currency
     */
    amount: number
}